    private void btn_CadastrarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_CadastrarActionPerformed
        // TODO add your handling code here:
      if( txt_nomeFuncionario.getText().isEmpty() || txt_num_carteiraFuncionario.getText().isEmpty()
      || txt_funcaoFuncionario.getText().isEmpty() || txt_foneFuncionario.getText().isEmpty() || txt_emailFuncionario.getText().isEmpty()
      || txt_senhaFuncionario.getText().isEmpty()){
      
          JOptionPane.showMessageDialog(null, "Preencha todos os campos!!");
      } 
      else{
      
          funcionario = new Funcionario();
          funcionario.setNomeFuncionario(txt_nomeFuncionario.getText());
          funcionario.setNum_carteiraFuncionario(txt_num_carteiraFuncionario.getText());
          funcionario.setTipoFuncionario(txt_funcaoFuncionario.getText());
          funcionario.setFoneFuncionario(txt_foneFuncionario.getText());
          funcionario.setEmailFuncionario(txt_emailFuncionario.getText());
          funcionario.setSenhaFuncionario(Integer.parseInt(txt_senhaFuncionario.getText()));
          
          try {
              funcionarioDAO.Cadastrar(funcionario);
              
              
          } catch (SQLException ex) {
              Logger.getLogger(FuncionarioView.class.getName()).log(Level.SEVERE, null, ex);
          }
          JOptionPane.showMessageDialog(null, "Cadastro de funcionário realizado com sucesso");
          atualizarTabelaFuncionario();
          atualizaUsuario();
          preparaSalvareCancelar();
          desativaCampos();
          LimpaCamposFuncionario();
       }
      
    }//GEN-LAST:event_btn_CadastrarActionPerformed

    
    
