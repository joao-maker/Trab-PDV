    private void atualizarTabelaFuncionario(){
    
        funcionario = new Funcionario();
        try {
            listaFuncionario = funcionarioDAO.ListaFuncionario();
        } catch (SQLException ex) {
            Logger.getLogger(FuncionarioView.class.getName()).log(Level.SEVERE, null, ex);
        }
        
        String dados[][] = new String[listaFuncionario.size()][7];
        int i = 0;
        
        for(Funcionario funcionario : listaFuncionario){
        
            dados[i][0] = String.valueOf(funcionario.getCodFuncionario());
            dados[i][1] = funcionario.getNomeFuncionario();
            dados[i][2] = funcionario.getNum_carteiraFuncionario();
            dados[i][3] = funcionario.getTipoFuncionario();
            dados[i][4] = funcionario.getFoneFuncionario();
            dados[i][5] = funcionario.getEmailFuncionario();
            dados[i][6] = String.valueOf(funcionario.getSenhaFuncionario());
            i++;
        }
        String tituloColuna[] = {"id","Nome","Numero_Carteira","Função","Fone","Email","Senha"};
        DefaultTableModel tabelaFuncionario = new DefaultTableModel();
        tabelaFuncionario.setDataVector(dados, tituloColuna);
        tbl_Funcionario.setModel(new DefaultTableModel(dados,tituloColuna){
        
            boolean[] canEdit = new boolean[]{
            
                false,false,false,false,false,false,false
            };
            @Override
            public boolean isCellEditable(int rowIndex, int columnIndex){
                return canEdit[columnIndex];
            }
        });
        
        tbl_Funcionario.getColumnModel().getColumn(0).setPreferredWidth(100);
        tbl_Funcionario.getColumnModel().getColumn(1).setPreferredWidth(600);
        tbl_Funcionario.getColumnModel().getColumn(2).setPreferredWidth(300);
        tbl_Funcionario.getColumnModel().getColumn(3).setPreferredWidth(200);
        tbl_Funcionario.getColumnModel().getColumn(4).setPreferredWidth(200);
        tbl_Funcionario.getColumnModel().getColumn(5).setPreferredWidth(500);
        tbl_Funcionario.getColumnModel().getColumn(6).setPreferredWidth(200);
        
        
        DefaultTableCellRenderer centralizado = new DefaultTableCellRenderer();
        centralizado.setHorizontalAlignment(SwingConstants.CENTER);
        tbl_Funcionario.getColumnModel().getColumn(0).setCellRenderer(centralizado);
        tbl_Funcionario.setRowHeight(25);
        tbl_Funcionario.updateUI();
    }
    
