    private void btn_CadastrarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_CadastrarActionPerformed
        // TODO add your handling code here:
        if(txt_nomeCliente.getText().isEmpty() || txt_tipoPessoa.getText().isEmpty() || txt_emailCliente.getText().isEmpty()
        || txt_fone1Cliente.getText().isEmpty() || txt_ruaCliente.getText().isEmpty() || txt_num_enderecoCliente.getText().isEmpty()
        || txt_bairroCliente.getText().isEmpty() ){
                
                JOptionPane.showMessageDialog(null, "Preencha todos os campos obrigatorios!!");
            
        }
        else{
        
            cliente = new Cliente();
            cliente.setNomeCliente(txt_nomeCliente.getText());
            cliente.setTipoPessoa(txt_tipoPessoa.getText());
            cliente.setCpfCliente(txt_cpfCliente.getText());
            cliente.setCnpjCliente(txt_cnpjCliente.getText());
            cliente.setEmail(txt_emailCliente.getText());
            cliente.setFone1(txt_fone1Cliente.getText());
            cliente.setFone2(txt_fone2Cliente.getText());
            cliente.setFone3(txt_fone3Cliente.getText());
            cliente.setRuaCliente(txt_ruaCliente.getText());
            cliente.setNum_enderecoCliente(Integer.parseInt(txt_num_enderecoCliente.getText()));
            cliente.setBairroCliente(txt_bairroCliente.getText());
            
            try {
                clienteDAO.Cadastrar(cliente);
            } catch (SQLException ex) {
                Logger.getLogger(ClienteView.class.getName()).log(Level.SEVERE, null, ex);
            }
            JOptionPane.showMessageDialog(null, "Cliente cadastrado com sucesso");
            LimpaCamposCliente();
            atualizarTabelaCliente();
            preparaSalvareCancelar();
            desativaCampos();
        }
    }//GEN-LAST:event_btn_CadastrarActionPerformed

