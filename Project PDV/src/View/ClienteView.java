    public void atualizarTabelaCliente(){
    
        cliente = new Cliente();
        try {
            listaCliente = clienteDAO.ListaCliente();
        } catch (SQLException ex) {
            Logger.getLogger(ClienteView.class.getName()).log(Level.SEVERE, null, ex);
        }
        
        String dados[][] = new String[listaCliente.size()][12];
        int i = 0;
        for (Cliente cliente : listaCliente) {
        
            dados[i][0] = String.valueOf(cliente.getCodCliente());
            dados[i][1] = cliente.getNomeCliente();
            dados[i][2] = cliente.getTipoPessoa();
            dados[i][3] = cliente.getCpfCliente();
            dados[i][4] = cliente.getCnpjCliente();
            dados[i][5] = cliente.getEmail();
            dados[i][6] = cliente.getFone1();
            dados[i][7] = cliente.getFone2();
            dados[i][8] = cliente.getFone3();
            dados[i][9] = cliente.getRuaCliente();
            dados[i][10] = String.valueOf(cliente.getNum_enderecoCliente());
            dados[i][11] = cliente.getBairroCliente();
            i++;
        }
        String tituloColuna[] = {"id","Nome","Tipo Pessoa","CPF","CNPJ","Email","Fone 1","Fone 2","Fone 3","Rua","Numero","Bairro"};
        DefaultTableModel tabelaCliente = new DefaultTableModel();
        tabelaCliente.setDataVector(dados, tituloColuna);
        tbl_Cliente.setModel(new DefaultTableModel(dados,tituloColuna){
        
            boolean[] canEdit = new boolean[]{
            
                false,false,false,false,false,false,false,false,false,false,false,false
            };
            
            @Override
            public boolean isCellEditable(int rowIndex, int columnIndex){
                return canEdit[columnIndex];
            }
        });
        
        tbl_Cliente.getColumnModel().getColumn(0).setPreferredWidth(100);
        tbl_Cliente.getColumnModel().getColumn(1).setPreferredWidth(300);
        tbl_Cliente.getColumnModel().getColumn(2).setPreferredWidth(100);
        tbl_Cliente.getColumnModel().getColumn(3).setPreferredWidth(200);
        tbl_Cliente.getColumnModel().getColumn(4).setPreferredWidth(200);
        tbl_Cliente.getColumnModel().getColumn(5).setPreferredWidth(300);
        tbl_Cliente.getColumnModel().getColumn(6).setPreferredWidth(150);
        tbl_Cliente.getColumnModel().getColumn(7).setPreferredWidth(150);
        tbl_Cliente.getColumnModel().getColumn(8).setPreferredWidth(150);
        tbl_Cliente.getColumnModel().getColumn(9).setPreferredWidth(200);
        tbl_Cliente.getColumnModel().getColumn(10).setPreferredWidth(100);
        tbl_Cliente.getColumnModel().getColumn(11).setPreferredWidth(200);
        
        DefaultTableCellRenderer centralizado = new DefaultTableCellRenderer();
        centralizado.setHorizontalAlignment(SwingConstants.CENTER);
        tbl_Cliente.getColumnModel().getColumn(0).setCellRenderer(centralizado);
        tbl_Cliente.setRowHeight(25);
        tbl_Cliente.updateUI();
    }
    
    
